package com.javaex.ex01;

public class Ex01 {
	
	//가장 먼저 실행되는 함수 main
	public static void main(String[] args) {
		
		//변수 선언과 초기화
		int myAge;
		myAge = 25;
		
		//출력
		System.out.println(myAge);
		
		//값 변경
		myAge = 100;
		 
		//변경된 값으로출력
		System.out.println(myAge);
		
	}
}
