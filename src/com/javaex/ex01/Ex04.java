package com.javaex.ex01;

public class Ex04 {
	
	public static void main(String[] args) {
		
		//논리형 변수
		boolean b01 = true;
		boolean b02 = false;
		
		System.out.println(b01);
		System.out.println(b02);
		
		//boolean사용법 예시
		int a = 3;
		int b = 5;
		//a가 b보다 크면 true 작으면false
		boolean rsult = a > b;
		System.out.println(rsult);
		
		
		
	}

}
